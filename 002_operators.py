"""
In Python programming, Operators in general are used to perform operations on values and variables. 
These are standard symbols used for the purpose of logical and arithmetic operations.

Key point to remember:
OPERATORS: These are the special symbols. Eg- + , * , /, etc.
OPERAND: It is the value on which the operator is applied.


1] Arithmetic operators
  => +	Addition	
    Example:
      x + y	
  => -	Subtraction	
    Example:
      x - y
  => *	Multiplication	
    Example:
      x * y	
  => /	Division	
    Example:
      x / y	
  => %	Modulus	
    Example:
      x % y	
  => **	Exponentiation	
    Example:
      x ** y	
  => //	Floor division	
    Example:
      x // y


2] Assignment operators
  => =	
    Example:
      x = 5, Same As	x = 5	

  => +=	 
    Example:
      x+= 3 Same As	x = x + 3	

  => -=	
    Example:
      x -= 3 Same As	x = x - 3	

  => *=	
    Example:
      x *= 3 Same As	x = x * 3	

  => /=	
    Example:
      x /= 3 Same As	x = x / 3	

  => %=	
    Example:
      x %= 3 Same As	x = x % 3	

  => //=	
  Example:
    x //= 3 Same As	x = x // 3

  => **=	
    Example:
      x **= 3 Same As	x = x ** 3

      
3] Comparison operators
  => ==	Equal	
    Example:
      x == y	

  => !=	Not equal	
    Example:
      x != y	

  => >	Greater than	
    Example:
      x > y	

  => <	Less than	
    Example:
      x < y	

  => >=	Greater than or equal to	
    Example
      x >= y	
      
  => <=	Less than or equal to	
    Example:
      x <= y

      
4] Logical operators
  => and 	=> True if both statements are true	
     Example:
      x < 5 and  x < 10	

  => or	=> True if one of the statements is true	
     Example:
      x < 5 or x < 4

  => not =>	Reverse the result, returns False if the result is true	
    Example:
      not(x < 5 and x < 10)

      
5] Identity operators
  => is 	=> True if both variables are the same object	
    Example:
      x is y

  => is not	=> True if both variables are not the same object	
    Example:
      x is not y	


6] Membership operators
  => in 	Returns True if a sequence with the specified value is present in the object	x in y	
    Example:
    nums = [1,2,3,4,5,6,7,8,9]
      `2 in nums` returns True

  => not in	Returns True if a sequence with the specified value is not present in the object	x not in y
    Example:
      nums = [1,2,3,4,5,6,7,8,9]
      `2 not in nums` returns False
"""
